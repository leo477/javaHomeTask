package com.itea.dimka.lection1;

public class EvenUneven {
    public static void main(String[] args) {
        int a=4;
        if (a % 2 == 0)
        {
            System.out.println("a is even number. a = " + a);
        } else {
            System.out.println("a is uneven number. a = " + a);
        }
    }
}
